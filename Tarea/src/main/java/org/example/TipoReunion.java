package org.example;

/**
 * La enumeración TipoReunion define los diferentes tipos de reuniones que se pueden organizar.
 * Permite categorizar las reuniones en tres tipos principales: técnica, marketing u otro.
 */
public enum TipoReunion {

    /** Reunión técnica, normalmente relacionada con desarrollo o aspectos tecnológicos. */
    TECNICA,

    /** Reunión de marketing, generalmente para discutir estrategias de ventas o promoción. */
    MARKETING,

    /** Reunión de otro tipo que no encaja en las categorías anteriores. */
    OTRO;
}

